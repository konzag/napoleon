def clear_ish():
    """Εκτύπωσε μερικές κενές γραμμές για εφέ «καθαρής οθόνης»."""
    print("\n" * 2)


def print_divider(color_char="=", width=55):
    """Εκτύπωσε μια διακοσμητική γραμμή στην οθόνη."""
    print(color_char * width)


def slow_print(text, delay=0.03):
    """
    Εκτύπωσε κείμενο ένα γράμμα τη φορά — σαν το text crawl του Star Wars!
    Το sys.stdout.flush() αναγκάζει κάθε γράμμα να εμφανίζεται αμέσως.
    """
    for char in text:
        sys.stdout.write(char)
        sys.stdout.flush()
        time.sleep(delay)
    print()  # Μετακίνηση στην επόμενη γραμμή όταν τελειώσει


def beep(frequency, duration_ms=300):
    """
    Παίξε έναν ήχο χρησιμοποιώντας το ενσωματωμένο ηχείο του υπολογιστή.
    frequency   = πόσο ΨΗΛΗ είναι η νότα (Hz)
    duration_ms = πόσο παίζει σε χιλιοστά του δευτερολέπτου
    """
    if SOUND_AVAILABLE:
        # winsound.Beep(συχνότητα, διάρκεια) — αυτό είναι όλο!
        winsound.Beep(frequency, duration_ms)
    else:
        # Αν ο ήχος δεν είναι διαθέσιμος, εμφάνισε μουσικό σύμβολο
        print(f"  ♪ (παίζω νότα στα {frequency} Hz) ♪")


def print_animal_fact(animal):
    """Εμφάνισε ένα όμορφα μορφοποιημένο γεγονός για ένα ζώο."""
    print()
    print_divider("-")
    print(f"  {animal['emoji']}  {animal['name'].upper()}  {animal['emoji']}")
    print_divider("-")
    print()
    # Το textwrap θα ήταν πιο τακτοποιημένο αλλά το κρατάμε απλό!
    # Χωρίζουμε τα μεγάλα κείμενα χειροκίνητα σε αναγνώσιμα κομμάτια
    words = animal['fact'].split()
    line = "  "
    for word in words:
        if len(line) + len(word) + 1 > 56:
            print(line)
            line = "  " + word + " "
        else:
            line += word + " "
    if line.strip():
        print(line)
    print()
    print(f"  🔊 Άκου τη μουσική νότα του {animal['name']}...")
    beep(animal['sound_note'], 400)
    print_divider("-")


