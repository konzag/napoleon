# ============================================================
#  ΚΥΡΙΟΣ ΒΡΟΧΟΣ
#  Αυτή είναι η καρδιά του προγράμματος!
#  Ένας βρόχος "while True" τρέχει για πάντα — μέχρι να του πούμε να σταματήσει.
# ============================================================

def main():
    """Κύριος βρόχος προγράμματος — εμφανίζει το μενού και χειρίζεται τις επιλογές."""

    # Εμφάνισε την εισαγωγή μια φορά
    show_intro()

    # Αυτός ο βρόχος συνεχίζει μέχρι ο Ναπολέων να επιλέξει έξοδο
    while True:
        clear_ish()
        print_divider("=")
        print("  ⚔️   ΤΖΕΝΤΆΙ ΕΞΕΡΕΥΝΗΤΗΣ ΦΥΣΗΣ — ΚΥΡΙΟ ΜΕΝΟΥ   ⚔️")
        print_divider("=")
        print()
        print(PLANT_ART)
        print()
        print("  Τι θα ήθελες να κάνεις, Παδαβάν;")
        print()
        print("  1. 🐾  Μάθε για ένα ΖΩΟ")
        print("  2. 🌿  Μάθε για ένα ΦΥΤΟ")
        print("  3. 🎵  Παίξε μια ΜΟΥΣΙΚΗ ΝΟΤΑ")
        print("  4. 🚪  Έξοδος (αντίο!)")
        print()
        print_divider("-")
        print("  ✨ Η Δύναμη να είναι μαζί σου, Ναπολέων! ✨")
        print_divider("-")
        print()

        # Το input() περιμένει ο Ναπολέων να γράψει κάτι και να πατήσει Enter
        choice = input("  Γράψε 1, 2, 3, ή 4: ").strip()

        if choice == '1':
            animal_menu()

        elif choice == '2':
            plant_menu()

        elif choice == '3':
            play_music_menu()

        elif choice == '4':
            # Ακολουθία αποχαιρετισμού!
            clear_ish()
            print_divider("*")
            print()
            slow_print("  Αντίο, Παδαβάν Ναπολέων!", 0.05)
            time.sleep(0.3)
            slow_print("  Συνέχισε να εξερευνάς τα θαύματα της φύσης.", 0.04)
            slow_print("  Η Δύναμη θα σε καθοδηγεί πάντα. 🌟", 0.04)
            print()
            # Παίξε μια μελωδία αποχαιρετισμού
            beep(392, 200)
            beep(330, 200)
            beep(262, 400)
            print()
            print_divider("*")
            print()
            # Το break βγαίνει από τον βρόχο while — το πρόγραμμα τελειώνει
            break

        else:
            # Αν ο Ναπολέων γράψει κάτι απροσδόκητο, θυμίζουμε ευγενικά
            print()
            print("  Χμ... αυτό δεν φαίνεται σωστό, Παδαβάν.")
            print("  Δοκίμασε να γράψεις 1, 2, 3, ή 4!")
            time.sleep(1.5)


# ============================================================
#  ΣΗΜΕΙΟ ΕΙΣΟΔΟΥ
#  Αυτό το ειδικό μπλοκ σημαίνει: "τρέξε το main() μόνο αν εκτελέσαμε ΑΥΤΟ το αρχείο"
#  (όχι αν κάποιο άλλο αρχείο το εισήγαγε ως module)
# ============================================================
if __name__ == "__main__":
    main()
